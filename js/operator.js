let a = 5;
let b = 4;
//arithmatic operator
console.log(a+b);
console.log(a**b);
console.log(a%b);
console.log(++a);//6
//assignment operator
console.log(a+=b);//10
console.log(a/=b);//2.5

//comparison operator
console.log(a==b);//false
console.log(a!=b);//true
console.log(a>b);//true
console.log(a===b);//false
console.log(a!==b);//true
let x = 5;
let y = "5";
console.log(x==y);//true
console.log(x===y);//false
console.log(x!==y);//true

//logical operator
let p = 5;
let q = 6;
console.log(p>q && p<q);//false
console.log(p>q || p<q);//true
console.log(!(p>q));//true



